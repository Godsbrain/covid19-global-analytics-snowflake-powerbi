-- Track cumulative vaccinations by country.
SELECT
    DEA.CONTINENT,
    DEA.LOCATION,
    DEA.DATE,
    DEA.POPULATION,
    VAC.NEW_VACCINATIONS,

    SUM(VAC.NEW_VACCINATIONS)
    OVER
    (
        PARTITION BY DEA.LOCATION
        ORDER BY DEA.DATE
    ) AS ROLLING_PEOPLE_VACCINATED

FROM PORTFOLIO_PROJECT.RAW.COVID_DEATHS DEA
JOIN PORTFOLIO_PROJECT.RAW.COVID_VACCINATIONS VAC
    ON DEA.LOCATION = VAC.LOCATION
    AND DEA.DATE = VAC.DATE
WHERE DEA.CONTINENT IS NOT NULL
ORDER BY DEA.LOCATION, DEA.DATE;

-- Use a CTE to calculate population vaccination percentages.
WITH POPVSVAC AS
(
    SELECT
        DEA.CONTINENT,
        DEA.LOCATION,
        DEA.DATE,
        DEA.POPULATION,
        VAC.NEW_VACCINATIONS,

        SUM(VAC.NEW_VACCINATIONS)
        OVER
        (
            PARTITION BY DEA.LOCATION
            ORDER BY DEA.DATE
        ) AS ROLLING_PEOPLE_VACCINATED

    FROM PORTFOLIO_PROJECT.RAW.COVID_DEATHS DEA
    JOIN PORTFOLIO_PROJECT.RAW.COVID_VACCINATIONS VAC
        ON DEA.LOCATION = VAC.LOCATION
        AND DEA.DATE = VAC.DATE
    WHERE DEA.CONTINENT IS NOT NULL
)

SELECT *,
       (ROLLING_PEOPLE_VACCINATED / NULLIF(POPULATION,0))*100
       AS PERCENT_VACCINATED
FROM POPVSVAC;


-- Create a temporary table for vaccination analysis.
CREATE OR REPLACE TEMPORARY TABLE PERCENT_POPULATION_VACCINATED AS

SELECT
    DEA.CONTINENT,
    DEA.LOCATION,
    DEA.DATE,
    DEA.POPULATION,
    VAC.NEW_VACCINATIONS,

    SUM(VAC.NEW_VACCINATIONS)
    OVER
    (
        PARTITION BY DEA.LOCATION
        ORDER BY DEA.DATE
    ) AS ROLLING_PEOPLE_VACCINATED

FROM PORTFOLIO_PROJECT.RAW.COVID_DEATHS DEA
JOIN PORTFOLIO_PROJECT.RAW.COVID_VACCINATIONS VAC
    ON DEA.LOCATION = VAC.LOCATION
    AND DEA.DATE = VAC.DATE;

-- Calculate vaccination percentage using the temporary table.
SELECT *,
       (ROLLING_PEOPLE_VACCINATED / NULLIF(POPULATION,0))*100
       AS PERCENT_VACCINATED
FROM PERCENT_POPULATION_VACCINATED;

