-- Create country-level COVID metrics.
CREATE OR REPLACE VIEW PORTFOLIO_PROJECT.ANALYTICS.VW_COUNTRY_STATS AS

SELECT
    LOCATION,
    CONTINENT,
    POPULATION,

    MAX(TOTAL_CASES) AS TOTAL_CASES,
    MAX(TOTAL_DEATHS) AS TOTAL_DEATHS,

    MAX(TOTAL_CASES/NULLIF(POPULATION,0))*100
        AS PERCENT_POPULATION_INFECTED,

    MAX(TOTAL_DEATHS/NULLIF(TOTAL_CASES,0))*100
        AS DEATH_PERCENTAGE

FROM PORTFOLIO_PROJECT.RAW.COVID_DEATHS
WHERE CONTINENT IS NOT NULL

GROUP BY
    LOCATION,
    CONTINENT,
    POPULATION;

-- Create global COVID trends by date.
CREATE OR REPLACE VIEW PORTFOLIO_PROJECT.ANALYTICS.VW_GLOBAL_DAILY AS

SELECT
    DATE,

    SUM(NEW_CASES) AS DAILY_CASES,

    SUM(NEW_DEATHS) AS DAILY_DEATHS,

    SUM(NEW_DEATHS)
/ NULLIF(SUM(NEW_CASES),0) *100
        AS DAILY_DEATH_PERCENTAGE

FROM PORTFOLIO_PROJECT.RAW.COVID_DEATHS

WHERE CONTINENT IS NOT NULL

GROUP BY DATE;

-- Create cumulative vaccination analysis.
CREATE OR REPLACE VIEW PORTFOLIO_PROJECT.ANALYTICS.VW_VACCINATION_PROGRESS AS

SELECT

    DEA.CONTINENT,
    DEA.LOCATION,
    DEA.DATE,
    DEA.POPULATION,

    VAC.NEW_VACCINATIONS,

    SUM(VAC.NEW_VACCINATIONS)
    OVER(
        PARTITION BY DEA.LOCATION
        ORDER BY DEA.DATE
    ) AS ROLLING_PEOPLE_VACCINATED,

    (
        SUM(VAC.NEW_VACCINATIONS)
        OVER(
            PARTITION BY DEA.LOCATION
            ORDER BY DEA.DATE
        )
    )
    /NULLIF(DEA.POPULATION,0)*100

    AS PERCENT_VACCINATED

FROM PORTFOLIO_PROJECT.RAW.COVID_DEATHS DEA

JOIN PORTFOLIO_PROJECT.RAW.COVID_VACCINATIONS VAC

ON DEA.LOCATION = VAC.LOCATION
AND DEA.DATE = VAC.DATE

WHERE DEA.CONTINENT IS NOT NULL;


-- Create continent-level metrics.
